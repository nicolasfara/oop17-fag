/**
 * Enum that models all type of entities in the game.
 */
public enum FagType {
    /**
     * Player type.
     */
    PLAYER,
    /**
     * Wall type.
     */
    WALL,
    /**
     * Simple zombie type.
     */
    SIMPLE_ZOMBIE,
    /**
     * Advance zombie type.
     */
    ADVANCE_ZOMBIE
}
